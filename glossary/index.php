/* Display the parsed and sorted names into their own tables */
function make_tables($letter_name, $letter) {
	if (empty($letter)) {
		echo " ";
	} else {
		echo "<h2 id='results_h2'>" . $letter_name . "</h2>";
		echo "<table class='results_table'>";
		echo "<tr>";
		
		natsort($letter);
		
		$col_counter = 0;
		foreach ($letter as $letter_output) {			
				if ($col_counter == 3) {
					echo "</tr><tr>";
					$col_counter = 0;
				}
				else {
					$directory = "articles/";
					$add_dashes = str_replace(' ', '-', $letter_output);
					$add_extension = ".html";
					$replace_filename = $directory.$add_dashes.$add_extension;
					echo "<td> <span> <a href='" . $replace_filename . "'>". $letter_output . "</a></span></td>";
					$col_counter++;
				}
		}
	echo "</tr>";
	echo "</table>";
	}
